    public boolean XoaSanPham(String MaSanPham) {
        String cautruyvan = "delete from SanPham where MaSanPham=" + MaSanPham;
        String ctvKiemThu = "select count(MaCTHD) as SoChiTietHoaDon\n" +
                            "from SanPham join ChiTietHoaDon\n" +
                            "on SanPham.MASANPHAM = ChiTietHoaDon.MASANPHAM\n" +
                            "where SanPham.MaSanPham= " + MaSanPham;
        ResultSet rs1 = khang.com.connection.ExcuteQueryGetTable(ctvKiemThu);
        String ctvKiemThu2 =    "select count(MaCTPN) as SoChiTietPhieuNhap\n" +
                                "from SanPham join ChiTietPhieuNhap \n" +
                                "on SanPham.MASANPHAM = ChiTietPhieuNhap.MASANPHAM\n" +
                                "where SanPham.MaSanPham= " + MaSanPham;
        ResultSet rs2 = khang.com.connection.ExcuteQueryGetTable(ctvKiemThu2);
        int so1 = 0, so2 = 0;

        try {
            if (rs1.next()) {
                so1 = rs1.getInt("SoChiTietHoaDon");
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
        try {

            if (rs2.next()) {
                so2 = rs2.getInt("SoChiTietPhieuNhap");
                if (rs2.getInt("SoChiTietPhieuNhap") == 0 && so1 == 0) {
                    khang.com.connection.ExcuteQueryUpdateDB(cautruyvan);
                    System.out.println("Đã xóa");
                    LayDuLieuSanPham("1");
                    return true;
                } else {
                    ThongBao("Không thể xóa bởi có trong " + so1 + "  Chi tiết hóa đơn hóa đơn \n và có trong "
                            + so2 + "  Chi tiết phiếu Nhập", "Báo lỗi", 2);
                }
            }

        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
        return false;
    }
